public class Loops {
    public static void main(String args[]){
        //While
        int i = 1;
        while(i <= 9){
            System.out.println(i);
            i++;
        }

        System.out.println("DONE");

        //for
        for(int j = 1; j <= 9; j++){
            System.out.println(j);
        }
        System.out.println("DONE");

    }
}
